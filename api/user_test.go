func randomUser(t *testing.T) (user db.User, password string) {
	password = util.RandomString(6)
	hashedPassword, err := util.HashPassword(password)
	require.NoError(t, err)

	user = db.User{
		Username: util.RandomAccountOwner(),
		Password: hashedPassword,
		FullName: util.RandomAccountOwner(),
		Email:    util.RandomEmail(),
	}
	return
}

func requireEqualUser(t *testing.T, expected db.User, body *bytes.Buffer) {
	data, err := io.ReadAll(body)
	require.NoError(t, err)

	var gotData map[string]interface{}
	err = json.Unmarshal(data, &gotData)
	require.NoError(t, err)

	userData, ok := gotData["Data"].(map[string]interface{})["user"].(map[string]interface{})
	require.True(t, ok, "failed to access the 'account' element in the JSON")

	// Parse the "created_at" field as a time.Time value using time.Parse()
	createdAtStr := userData["created_at"].(string)
	createdAt, err := time.Parse(time.RFC3339, createdAtStr)
	require.NoError(t, err, "failed to parse created_at time")

	// Parse the "password_changed_at" field as a time.Time value using time.Parse()
	passwordChangedAtStr := userData["password_changed_at"].(string)
	passwordChangedAt, err := time.Parse(time.RFC3339, passwordChangedAtStr)
	require.NoError(t, err, "failed to parse password_changed_at time")

	gotUser := db.User{
		Username:          userData["username"].(string),
		Email:             userData["email"].(string),
		Password:          userData["password"].(string),
		FullName:          userData["full_name"].(string),
		PasswordChangedAt: passwordChangedAt,
		CreatedAt:         createdAt,
	}

	require.Equal(t, expected, gotUser)
}
